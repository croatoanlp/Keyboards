//Lenny Face output ( ͡° ͜ʖ ͡°) ; ALARM! ALARM!!! output
enum custom_keycodes{ COPYPASTA = SAFE_RANGE, ALARM};

bool process_record_user(uint16_t keycode, keyrecord_t *record){
switch(keycode){
	case COPYPASTA:
		if(record->event.pressed){
			send_unicode_string("( ͡° ͜ʖ ͡°)");
		break;
		}	
	case ALARM: 
		if(record->event.pressed){
			send_string("Hi, ich bin bei einem Feuerwehreinsatz. Ich melde mich wenn ich wieder da bin."SS_TAP(X_ENT));
		break;
		}
	}
	return true;
};


